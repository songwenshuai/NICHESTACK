void
Unicode( unsigned short *Dest, unsigned char *Src )
{
#ifdef __BIG_ENDIAN
    while ( *Src ) {
	*Dest++ = *Src++ << 8;
    }
    *Dest = 0;
#else
    while ( *Src ) {
	*Dest++ = *Src++;
    }
    *Dest = 0;
#endif
}

void
md4_test( char *Password, unsigned char *Expected )
{
    unsigned short UnicodePassword[256];
    unsigned char Hash[16];
    unsigned int len;
    int ch;
    MD4_CTX MD4_Context;

    /*
    ** Create a Unicode version of the Password string
    */
    Unicode( UnicodePassword, (unsigned char *)Password );

    for ( len=0; UnicodePassword[len]; len++ )
	;
   
    MD4Init( &MD4_Context);
    MD4Update( &MD4_Context, (unsigned char *)UnicodePassword, len*2 );
    MD4Final( Hash, &MD4_Context);

    if ( memcmp( Expected, Hash, 16 ) == 0 )
	return;

    Printu_Net( "FAILED %s\n\tEXPECTED ", Password );
    for ( ch=0; ch<16;ch++ )
	Printu_Net(" %02x", Expected[ch] );
    Printu_Net( "\n\tGOT      ");
    for ( ch=0; ch<16;ch++ )
	Printu_Net(" %02x", Hash[ch] );
    Printu_Net( "\n");
}

